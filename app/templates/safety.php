		<div class="safetyh1">Helping Others</div>
			<div class="safetyh2"><img class="safety-title" src="images/safety_title_bystander_intervention.webp" />
				<p class="morepara">As a witness to harassment or inappropriate behaviour there's a technique known as "The 5D Method" you can use to help others.</p>
				<div class="safetyh2 scollapsible2"><i class="fa-regular fa-face-surprise s_icon"></i>Distract
					<div class="safetymore">
						<div class="morepara">
						Engaging the victim in conversation or diverting attention from the harasser can help defuse the situation and provide the victim with a sense of support. Here are some examples:
						<ul>
							<li>Strike up a conversation with the victim about a completely unrelated topic, such as asking for directions or talking about a shared interest.</li>
							<li>Pretend to know the victim and approach them with a friendly greeting like, "Hey, haven't seen you in ages! How have you been?"</li>
							<li>Cause a distraction, like dropping something loudly, to draw the harasser's attention away from the victim.</li>
						</ul>
						</div>
					</div>
				</div>
				<div class="safetyh2 scollapsible2"><i class="fa-solid fa-person-circle-check s_icon"></i>Delegate
					<div class="safetymore">
						<div class="morepara">
						Seeking help from a person of authority or involving others can provide a safer environment for the victim. Examples include:
						<ul>
							<li>Approach a security guard, bartender, or staff member and inform them about the situation.</li>
							<li>Ask a friend or another bystander to accompany you to help the victim or to keep an eye on the situation while you seek assistance.</li>
							<li>If necessary and with the victim's consent, involve the police or relevant authorities to address the harassment.</li>
						</ul>
						</div>
					</div>
				</div>
				<div class="safetyh2 scollapsible2"><i class="fa-solid fa-video s_icon"></i>Document
					<div class="safetymore">
						<div class="morepara">
						Recording the incident can provide evidence and support the victim's case. Make sure to prioritise safety and legality when documenting. Examples include:
						<ul>
							<li>Use your smartphone to discreetly record the harassment, ensuring that you capture the harasser's behaviour and any relevant details like their appearance or location.</li>
							<li>Take photos or videos that include landmarks or signage to help identify the location of the incident.</li>
							<li>Jot down notes about what you witnessed, including dates, times, locations, and any relevant descriptions.</li>
						</ul>
						</div>
					</div>
				</div>
				<div class="safetyh2 scollapsible2"><i class="fa-solid fa-person-circle-exclamation s_icon"></i>Direct
					<div class="safetymore">
						<div class="morepara">
						Confronting the harasser in a calm, assertive, and non-aggressive manner can discourage their behaviour. Be careful to prioritise safety and avoid escalating the situation. Examples include:
						<ul>
							<li>Approach the harasser and calmly say, "Excuse me, but your comments/actions are making this person uncomfortable. Please stop."</li>
							<li>Maintain eye contact and use a confident tone to communicate your disapproval of their behaviour.</li>
							<li>Use "I" statements to express your concern, such as saying, "I don't think it's appropriate to speak to someone like that."</li>
						</ul>
						</div>
				</div>
			</div>
			<div class="safetyh2 scollapsible2"><i class="fa-solid fa-hand-holding-heart s_icon"></i>Delay
				<div class="safetymore">
					<div class="morepara">
						
						Offering support to the victim after the incident demonstrates solidarity and helps them feel less isolated. Examples include:
						<ul>
							<li>Approach the victim after the harassment has subsided and ask if they're okay or if they need any assistance.</li>
							<li>Express empathy and let them know that you noticed the situation and are there to support them.</li>
							<li>Offer to stay with the victim or accompany them to a safe place, such as a private space or a friend's location.</li>
						</ul>	
					</div>
				</div>
