<h3>Create New Highlight - Help</h3>
<a class="btn-sm" href="<?php echo DOMAIN_ADMIN; ?>/plugin/lffevents/?addhighlight" class="btn">Back</a>
<br>
<br>
<h4>Title</h4>
<p>Enter the title of the new highight</p>
<h4>Subitle</h4>
<p>Enter the subtitle of the new highight</p>

<h4>Category</h4>
<p>Choose the category of the new highlight here, eg Venue Offer, Hotel Offer etc.  This setting will impact how the highlight will be shown in the app.</p>

<h4>Venue</h4>
<p>Choose the venue the highlight is associated with here.</p>

<h4>Description</h4>
<p>Enter descriptive text which will be displayed in the body of the highlight details inside the app.</p>

<h4>Start</h4>
<p>Enter the date & time from which the highlight should show in the app.</p>

<h4>End</h4>
<p>Enter the date & time which the highlight should be shown until</p>

<h4>Priority</h4>
<p>Highlights displayed within the app are shown sorted by category, then priority.  If it is desired to show one highlight before one which has the same category, set its priority to a lower value: 1 being the highest priority</p>

<h4>CTA Text</h4>
<p>Call To Action Text, for example: Message venue for more details</p>

<h4>CTA Link</h4>
<p>The web link clicking on the call to action text should take the app user to, eg fb.me/messagevenuemanager</p>

<h4>Offer Code</h4>
<p>If the new highlight is a special offer with an associated code, enter that here.</p>

<h4>Show?</h4>
<p>Ensure this box is enabled (slid to the right) to have the highlight show within the app. </p>

<h4>Save Highlight</h4>
<p>Once satisfied the new highlight details are correct, click Save Highlight to enter it into the system.  This will also automatically update the data stream used by the app.</p>