<br>
<h4>Service Name</h4>
<p>Enter the name of the service</p>

<h4>Category</h4>
<p>Choose the category the new service should appear under here</p>

<h4>Priority</h4>
<p>Services displayed within the app are shown sorted by category, then priority.  If it is desired to show one service before one which has the same category, set its priority to a lower value: 1 being the highest priority</p>

<h4>Address</h4>
<p>Enter the address of the service including postcode.</p>

<h4>Description</h4>
<p>Enter descriptive text which will be displayed in the body of the service details inside the app.</p>

<h4>Image</h4>
<p>Click on the Image text box to open the image picker which allows the service's associated image to be chosen</p>

<h4>Website</h4>
<p>Enter the service's website address here</p>

<h4>Facebook</h4>
<p>The new service's facebook link goes here</p>

<h4>Instagram</h4>
<p>If the new service has an Instagram page, insert the link to it here</p>

<h4>Show?</h4>
<p>Ensure this box is enabled (slide to the right) to have the service show within the app.  </p>

<h4>Save Service</h4>
<p>Once satisfied the new service details are correct, click Save Service to enter it into the system.  This will also automatically update the data stream used by the app.</p>